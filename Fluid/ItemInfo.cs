        /// <summary>
        /// Gets all block ids
        /// </summary>
        /// <returns>The list of all block ids</returns>
        public static List<BlockID> GetAllIDs()
        {
            List<BlockID> all = new List<BlockID>();

            Type t = typeof(BlockIDs);
            Queue<Type> types = new Queue<Type>();
            types.Enqueue(t);

            while (types.Count > 0)
            {
                Type cur = types.Dequeue();
                foreach (Type n in cur.GetNestedTypes())
                {
                    types.Enqueue(n);
                }

                foreach (FieldInfo field in cur.GetFields())
                {
                    object o = field.GetRawConstantValue();
                    all.Add((BlockID)o);
                }
            }

            return all;
        }

        /// <summary>
        /// Gets the corresponding block pack to a block id
        /// </summary>
        /// <param name="blockId">The block id</param>
        /// <returns>The corresponding block pack if found; otherwise null</returns>
        public static string GetBlockPack(BlockID blockId)
        {
            foreach (KeyValuePair<string, List<int>> blockPack in m_BlockPacks)
            {
                if (blockPack.Value.Contains((int)blockId))
                {
                    return blockPack.Key;
                }
            }

            return null;
        }

